package homework_week_2;

public class Course {

	int id;
	String courseName;
	String courseEducator;
	int percentCompletion;
	
	public Course() {
		
	}
	
	public Course(int id, String courseName, String courseEducator, int percentCompletion) {
		super();
		this.id = id;
		this.courseName = courseName;
		this.courseEducator = courseEducator;
		this.percentCompletion = percentCompletion;
	}
	
}
