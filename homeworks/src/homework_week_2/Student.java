package homework_week_2;

public class Student {

	int id;
	String studentName;
	String studentEmail;
	String adress;
	
	public Student() {
		
	}
	
	public Student(int id, String studentName, String studentEmail, String adress) {
		super();
		this.id = id;
		this.studentName = studentName;
		this.studentEmail = studentEmail;
		this.adress = adress;
	}
	
}
